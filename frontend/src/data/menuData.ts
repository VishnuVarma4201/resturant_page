// Function to get featured items for homepage
export const getFeaturedItems = () => {
  // Get one item from each category and add category information
  const featuredItems = [];
  
  // Add one item from starters
  if (menuData.starters.length > 0) {
    featuredItems.push({
      ...menuData.starters[0],
      category: "Starters"
    });
  }
  
  // Add one item from main course
  if (menuData.mainCourse.length > 0) {
    featuredItems.push({
      ...menuData.mainCourse[0],
      category: "Main Course"
    });
  }
  
  // Add one item from desserts
  if (menuData.desserts.length > 0) {
    featuredItems.push({
      ...menuData.desserts[0],
      category: "Desserts"
    });
  }
  
  // Add one item from beverages if we need more items
  if (featuredItems.length < 3 && menuData.beverages.length > 0) {
    featuredItems.push({
      ...menuData.beverages[0],
      category: "Beverages"
    });
  }
  
  return featuredItems;
};
